def main():
    json_path = os.path.join(os.path.dirname(__file__), 'data.json')
    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    updated = 0
    for cat in data:
        cat_id = cat['id']
        for q in cat['questions']:
            num = q['num']
            key = (cat_id, num)
            if key in PREP_ANSWERS:
                q['answer'] = PREP_ANSWERS[key]
                updated += 1
            else:
                print(f"WARNING: no PREP answer for {cat_id} Q{num} ({q['title']})")

    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Done! Updated {updated} questions to PREP format.")

if __name__ == '__main__':
    main()
